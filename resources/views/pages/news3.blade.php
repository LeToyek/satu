                            <p>
                            <h2>Cara Membuat Surat Izin Usaha Kecil Secara Online</h2><br>
                            <p>Mengapa penting bagi bisnis untuk memiliki IUMK? Salah satunya adalah agar memiliki jaminan
                                legalitas hukum. Sehingga, usahamu termasuk resmi atau legal dan dapat
                                dipertanggungjawabkan.

                                Selain itu, dengan memiliki IUMK akan lebih mudah bagi usaha mikro untuk mendapatkan
                                permodalan. Berikut ini adalah cara-cara membuat surat izin usaha kecil online yang bisa
                                kamu ikuti!</p><br>
                            <b>1. Siapkan Persyaratan Dokumennya</b><br>
                            Cara membuat surat izin usaha kecil yang pertama, kamu harus menyiapkan dokumen-dokumen
                            persyaratannya. Dokumen yang dibutuhkan adalah KTP, NIB, NPWP, KK, Pas Foto, Surat Pengantar
                            dari RT/RW dari lokasi usaha.

                            Apabila sudah menyiapkan sejak awal, maka saat pengisian formulir secara online menjadi lebih
                            lancar. Jadi, jangan sampai melewatkan dokumen di atas ya.
                            <br>
                            <b>2. Membuat Akun di OSS</b><br>
                            Untuk dapat membuat akun di website resmi OSS, kunjungi www.osss.go.id/pas/ dan klik pada menu
                            ‘Daftar’ di sisi atas sebelah kanan. Lalu, kamu bisa mengisikan data yang diminta dengan
                            informasi yang benar.

                            Jangan lupa masukkan kode captcha yang muncul sebelum klik button ‘daftar’ di sisi bawahnya.
                            Setelah itu, kamu bisa membuka e-mail yang sudah kamu daftarkan untuk melakukan aktivasi akun.

                            Akun pun teraktivasi, selanjutnya kamu harus melakukan verifikasi. Kamu akan menerima password
                            untuk mengakses akun OSS-mu via e-mail. Simpanlah password tersebut!
                            <br>
                            <b>3. Memproses Pengajuan IUMK</b><br>
                            Melansir dari Kompas, berikut ini rangkuman runtut cara membuat surat izin usaha perorangan.
                            Kembali ke website www.osss.go.id dan masuk dengan akun yang kamu buat tadi.

                            Lalu, klik menu ‘Perizinan Berusaha’ kemudian klik ‘Perseorangan’ dan pilihlah ‘Skala Usaha
                            Mikro’ dilanjut dengan klik ‘Pendaftaran NIB Perseorangan Mikro’. Langkah selanjutnya, klik
                            ‘Skala Usaha Kecil’, kemudian pilih ‘Pendaftaran NIB Perseorangan Kecil’.

                            Lakukan pengisian ‘Data Profil’ dan ‘Data Usaha’ dengan sebaik mungkin. Jangan lupa untuk tekan
                            simpan jika seluruh kolom formulir sudah terisi. Apabila kamu mempunyai lebih dari satu usaha,
                            bisa melakukan penambahan Data Usaha.

                            Langkah selanjutnya, kamu bisa mengisi formulir ‘Komitmen Prasarana Usaha’ untuk melakukan
                            pengajuan ‘Izin Lokasi dan Lingkungan’.
                            <br>
                            <b>4. Memproses NIB</b><br>
                            NIB adalah Nomor Induk Berusaha yang menjadi identitas dari pemilik usaha dalam pelaksanaan
                            kegiatan usahanya. Kamu harus memiliki NIB agar berhasil melakukan pengajuan surat izin usaha
                            pribadi.

                            Di sesi ini, akan ditampilkan draft NIB dan Izin usaha milikmu. Teliti terlebih dahulu apakah
                            sudah sesuai. Jika sudah sesuai maka klik centang di kotak disclaimer. Di sini kamu bisa
                            melakukan cetak Izin Usaha dengan QR code.

                            Demikian tips untuk cara membuat surat izin usaha kecil secara online. Semoga dapat bermanfaat
                            untuk usaha yang sedang kamu jalankan!
